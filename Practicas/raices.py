import math

def raiz(n):
    i = 0
    m = (len(bin(n))-1) // 2
    x = 1 << m
    y = (x**2+n)//(2*x)
    while x > y:
        (x,y) = (y,(y**2+n)//(2*y))
        i+=1
    return [x,i]

